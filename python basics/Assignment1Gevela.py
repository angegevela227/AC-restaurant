
# A program that will compute for the average of quizzes 
# Ask the user to enter the value of quizzes
# Display the result 

quiz_1 = int(input("Enter the value for the quiz 1: "))
quiz_2 = int(input("Enter the value for the quiz 2: "))
quiz_3 = int(input("Enter the value for the quiz 3: "))

average = (quiz_1+quiz_2+quiz_3)/3

print("The average scores of the quizzes is: ",average)